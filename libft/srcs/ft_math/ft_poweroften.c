#include "libft.h"

int		ft_poweroften(int n)
{
	static int tenpow[10] = {1, 10, 100, 1000, 10000, 100000, 1000000, \
							10000000, 100000000, 1000000000};

	return ((n >= 0 && n <= 10) ? tenpow[n] : 0);
}
